public class Floating_point_precision {
    public static void main(String[] args) {
        // Floating point precision and double point precision .
        int a=10;
        float b=33.84f;
        double c=44.344;
        double d=834.3454d;
        System.out.println("Integer value : "+ a/3);
        System.out.println("Floating value : " + b/10.0 );
        System.out.println("Double value : "+ c/12.4);
        System.out.println("Last Double value : "+ d/23.4);
    }
}
